#Number --> int float

x=100    #int type
y=10.5   #float type
s="welcome"   # string (str)/char type
b=True        # boolean (bool) Type (True or False)

# to know the type of variable

print (type(x))   #you will get <class 'int'>
print(type(y))    #you will get <class 'float'>
print(type(s))    #you will get <class 'str'>
print(type(b))    #you will get <class 'bool'>